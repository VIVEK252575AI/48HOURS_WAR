package DAY1;

import java.util.*;

class AddTwoNumByTakingInputs {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int a = sc.nextInt();  // take first number
        int b = sc.nextInt();  // take second number

        int sum = a + b;

        System.out.println("Sum = " + sum);
    }
}



